/* USER CODE BEGIN 4 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    if(GPIO_Pin == GPIO_PIN_13)
    {
        // ¡CAMBIO CLAVE ACÁ! Buscamos el SET (1 lógico) para el apriete en la Nucleo
        if(HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13) == GPIO_PIN_SET)
        {
            t_apriete = __HAL_TIM_GET_COUNTER(&htim2); // Arrancamos a contar
        }
        else // Si soltamos, el pin cae a RESET (0 lógico)
        {
            t_suelte = __HAL_TIM_GET_COUNTER(&htim2); // Frenamos de contar

            if(t_suelte >= t_apriete) {
                tiempo_presionado = t_suelte - t_apriete;
            } else {
                tiempo_presionado = (0xFFFFFFFF - t_apriete) + t_suelte;
            }

            if (tiempo_presionado > 200000) // Anti-rebote
            {
                if(tiempo_presionado >= 4000000 && tiempo_presionado <= 8000000)
                {
                    flag_parpadeo = 1;
                }
            }
        }
    }
}
